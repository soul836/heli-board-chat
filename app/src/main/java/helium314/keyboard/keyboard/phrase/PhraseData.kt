    fun getPhrasesByCategory(category: String): List<PhraseEntry> =
        phrases.filter { it.category == category }

    fun searchPhrases(query: String): List<PhraseEntry> {
        if (query.isBlank()) return phrases
        val q = query.lowercase()
        return phrases.filter { it.text.lowercase().contains(q) }
    }
